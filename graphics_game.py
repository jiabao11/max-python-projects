import turtle as t
import random
def triangle(side,color,fillcolor):
    t.color(color)
    t.fillcolor(fillcolor)
    t.begin_fill()
    for i in range(3):
        t.forward(side)
        t.left(120)
    t.end_fill()
def rectangle(length1,length2,color,fillcolor):
    t.color(color)
    t.fillcolor(fillcolor)
    t.begin_fill()
    for i in range(2):
        t.forward(length1)
        t.left(90)
        t.forward(length2)
        t.left(90)
    t.end_fill()
def move(x,y):
    t.penup()
    t.goto(x,y)
    t.pendown()
t.speed('fastest')
t.hideturtle()
